
public class Roll {
	public boolean isSkunk;
	public boolean isSkunkAndDeuce;
	public boolean isTwoSkunk;
	public int score;
	public int penalty;
	
	public Roll(boolean isSkunk, boolean isSkunkAndDeuce, boolean isTwoSkunk, int score, int penalty) {
		super();
		this.isSkunk = isSkunk;
		this.isSkunkAndDeuce = isSkunkAndDeuce;
		this.isTwoSkunk = isTwoSkunk;
		this.score = score;
		this.penalty = penalty;
	}
	
	
	
}
